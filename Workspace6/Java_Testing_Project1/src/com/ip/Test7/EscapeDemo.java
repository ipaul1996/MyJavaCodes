package com.ip.Test7;

public class EscapeDemo {
    public static void main(String[] args) {
    	System.out.println("Insert a tab:\tHere it is!");
        System.out.println("Insert a backspace: Back\bspace!");
        System.out.println("Insert a new line:\nLine 1\nLine 2\nLine 3");
        System.out.println("Insert a carriage return:\rCarriage Return");
        System.out.println("Insert a form feed:\fForm Feed");
        System.out.println("Insert a single quote:\'Single Quote\'");
        System.out.println("Insert a double quote: \"Double Quote\"");
        System.out.println("Insert a backslash: \\Backslash\\");
    }
}
